package egovframework.webco.vo;

public class SyrupCoupon {
	private String couponProdNo;
	private String couponNo;
	private String couponSDate;
	private String couponEDate;
	public String getCouponProdNo() {
		return couponProdNo;
	}
	public void setCouponProdNo(String couponProdNo) {
		this.couponProdNo = couponProdNo;
	}
	public String getCouponNo() {
		return couponNo;
	}
	public void setCouponNo(String couponNo) {
		this.couponNo = couponNo;
	}
	public String getCouponSDate() {
		return couponSDate;
	}
	public void setCouponSDate(String couponSDate) {
		this.couponSDate = couponSDate;
	}
	public String getCouponEDate() {
		return couponEDate;
	}
	public void setCouponEDate(String couponEDate) {
		this.couponEDate = couponEDate;
	}
	
	
}
